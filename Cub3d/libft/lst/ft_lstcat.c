#include "../libft.h"

void	ft_lstcat(t_list **lst1, t_list **lst2)
{
	t_list	*last;

	if (!*lst1)
	{
		*lst1 = *lst2;
		return ;
	}
	last = ft_lstlast(*lst1);
	last->next = *lst2;
}
