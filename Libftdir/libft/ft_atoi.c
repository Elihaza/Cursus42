#include "libft.h"

int		ft_atoi(const char *str)
{
	long i;
	long s;
	long r;

	i = 0;
	s = 1;
	r = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
			|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -s;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i++] - '0');
		if (r > 2147483647 && s > 0)
			return (-1);
		if (r > 2147483648 && s < 0)
			return (0);
	}
	return (r * s);
}
